DROP TABLE IF EXISTS ELECTION_SCORE;

CREATE TABLE ELECTION_SCORE  (
  PARTY VARCHAR(250) NOT NULL PRIMARY KEY,
  SEATS int NOT NULL,
  VOTES int NOT NULL
);

DROP TABLE IF EXISTS ELECTION;

CREATE TABLE ELECTION  (
  ID int NOT NULL PRIMARY KEY,
  YEAR int NOT NULL,
  TOT_SEATS int NOT NULL,
  ACTIVE BOOLEAN NOT NULL
);